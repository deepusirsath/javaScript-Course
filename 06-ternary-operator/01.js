//ternary operator
let age = 8;
console.log(age > 8 ? "coffiee" : "milk");

// && operator  //dono condition shi hona chahiye
let fname = "deepali";
if (fname[0] === "d" && age === 8) {
  console.log("you win");
} else {
  console.log("you lose");
}

// ||  operator //dono me se koi ek
if (fname[0] === "i" || age === 80) {
  console.log("you win");
} else {
  console.log("you lose");
}
